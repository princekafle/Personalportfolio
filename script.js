function shownavbar() {
    const sider = document.querySelector('.sidebar')
    sider.style.display = 'flex'

    const line =  document.querySelector('.line')
    line.style.display='block'
}

const hidenavbar=()=> {
    const sider = document.querySelector('.sidebar')
    sider.style.display = 'none'

    const line =  document.querySelector('.line')
    line.style.display='none'
}

